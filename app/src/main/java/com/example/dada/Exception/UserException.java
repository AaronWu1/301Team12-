package com.example.dada.Exception;

/**
 * The User exception.
 */
public class UserException extends Exception {

    /**
     * Instantiates a new User exception.
     */
    public UserException() {

    }

    /**
     * Instantiates a new User exception.
     *
     * @param message the message
     */
    public UserException(String message) {
        super(message);
    }
}
